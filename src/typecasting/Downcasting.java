package typecasting;

public class Downcasting {

	public static void main(String[] args) {

		// Downcasting :Manually: converting lower to higher....

		// converting Long to Int.....
		long l = 10;
		int i = (int) l;
		System.out.println(i);

		// converting double to float.....
		double d = 10.5d;
		float f = (float) d;
		System.out.println(f);

	}

}
